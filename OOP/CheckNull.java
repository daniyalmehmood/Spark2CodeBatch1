package OOP;

public class CheckNull {

    public static void checkNull(Integer a) {
        if (a == null) {
            System.out.println("Its null");
        } else {
            System.out.println("Its not null");
        }
    }

    public static void checkNull(Double a) {
        if (a == null) {
            System.out.println("Its null");
        } else {
            System.out.println("Its not null");
        }
    }

    public static void checkNull(Boolean a) {
        if (a == null) {
            System.out.println("Its null");
        } else {
            System.out.println("Its not null");
        }
    }

    public static void checkNull(String a) {
        if (a == null) {
            System.out.println("Its null");
        } else {
            System.out.println("Its not null");
        }
    }




}
